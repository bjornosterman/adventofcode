    public class Maze
    {
        private HashSet<int> _tiles;
        private int _startPos;
        private int _endPos;
        private Dictionary<int, int> _jumps;
        private HashSet<int> _tilesLeft;
        private int _createdLevels;
        private int _minTileX;
        private int _maxTileX;
        private int _minTileY;
        private int _maxTileY;

        private class Letter
        {
            public char Value;
            public int Position;
        }
        public Maze(string text)
        {
            var sr = new StringReader(text);
            string line;
            int y = 0;
            _tiles = new HashSet<int>();
            var letters = new List<Letter>();

            while ((line = sr.ReadLine()) != null)
            {
                for (int x = 0; x < line.Length; x++)
                {
                    var pos = y * 1000 + x;
                    var c = line[x];
                    switch (c)
                    {
                        case '#':
                        case ' ':
                            // ignore
                            break;
                        case '.':
                            _tiles.Add(pos);
                            break;
                        default:
                            letters.Add(new Letter { Value = c, Position = pos });
                            break;
                    }
                }
                y++;
            }

            var portals = new List<(string, int)>();

            int? PortalTile(int pos) => Sides(pos).Cast<int?>().FirstOrDefault(x => _tiles.Contains(x.Value));

            foreach (var letter in letters)
            {
                var tile = PortalTile(letter.Position);
                if (tile != null)
                {
                    var otherLetter = letters.First(x => x.Position == letter.Position - (tile - letter.Position));
                    var portalName =
                        letter.Position < otherLetter.Position
                        ? new string(new[] { letter.Value, otherLetter.Value })
                        : new string(new[] { otherLetter.Value, letter.Value });
                    switch (portalName)
                    {
                        case "AA":
                            _startPos = tile.Value;
                            break;
                        case "ZZ":
                            _endPos = tile.Value;
                            break;
                        default:
                            portals.Add((portalName, letter.Position));
                            break;
                    }
                }
            }

            _jumps = new Dictionary<int, int>();
            foreach (var group in portals.ToLookup(x => x.Item1))
            {
                var pair = group.ToArray();
                _jumps.Add(pair[0].Item2, PortalTile(pair[1].Item2).Value);
                _jumps.Add(pair[1].Item2, PortalTile(pair[0].Item2).Value);
            }

            _minTileX = _tiles.Min(x => x % 1000);
            _maxTileX = _tiles.Max(x => x % 1000);
            _minTileY = _tiles.Min(x => x / 1000);
            _maxTileY = _tiles.Max(x => x / 1000);
        }
        private static int[] Sides(int pos) => new[] { pos + 1, pos - 1, pos + 1000, pos - 1000 };
        private bool IsOuter(int pos)
        {
            int x = pos % 1000, y = pos % 1000000 / 1000;
            return x < _minTileX + 2 || x > _maxTileX - 2 || y < _minTileY + 2 || y > _maxTileY - 2;
        }

        private int Jump(int from)
        {
            int currentLevel = from / 1_000_000;
            if (_jumps.TryGetValue(from % 1_000_000, out var jmpTo))
            {
                var nextLevel = currentLevel + (IsOuter(from) ? -1 : 1);
                jmpTo += nextLevel * 1_000_000;
                if (nextLevel > _createdLevels)
                {
                    _createdLevels++;
                    _tilesLeft.UnionWith(_tiles.Select(x => _createdLevels * 1_000_000 + x));
                }
                return jmpTo;
            }
            return from;
        }

        public int Search()
        {
            _tilesLeft = new HashSet<int>(_tiles);
            var positions = new List<int> { _startPos };
            var nextPositions = new List<int>();
            int steps = 0;
            _createdLevels = 0;
            while (positions.Any())
            {
                steps++;
                foreach (var position in positions)
                {
                    foreach (var tryPos in Sides(position).Select(Jump))
                    {
                        if (tryPos == _endPos)
                        {
                            return steps;
                        }

                        if (_tilesLeft.Remove(tryPos))
                        {
                            nextPositions.Add(tryPos);
                        }
                    }
                }
                positions = nextPositions;
                nextPositions = new List<int>();
            }
            throw new Exception("What! Didn't find it, wierd...");
        }
    }

